'''1.让用户输入他的体重(kg)和身高(m),判断他的BMI，根据他的BMI指标合理的给出建议
 BMI = 体重 / 身高*身高
 BMI < 18.5    “你这么瘦,可以肆无忌惮的大吃大喝啦"
 y18.5 <= BMI < 25  “兄弟！你离模特就差八块腹肌了”
 25 <= BMI < 30  “控制你自己哦！脂肪有点多啦”
 BMI >= 30   “不能再吃了！跑几圈去吧，你也可以是男神”
'''
weight = int(input("请输入您的体重kg:"))
height = float(input("请输入您的身高m:"))

BMI= weight/(height*height)

if BMI< 18.5:
	print("你这么瘦,可以肆无忌惮的大吃大喝啦")
elif 18.5 <= BMI < 25:
	print("兄弟！你离模特就差八块腹肌了") 
elif 25 <= BMI < 30 :
	print("控制你自己哦！脂肪有点多啦")
else :
	print("不能再吃了！跑几圈去吧，你也可以是男神")
