
l = ["172.16.3.1", "172.16.1.5", "172.15.2.0", "172.16.3.1", "172.16.3.1", "172.16.1.5"]
a = 0
b = 0
c = 0

for i in l:
	if i == "172.16.3.1":
		a +=1
	elif i == "172.16.1.5":
		b +=1
	else:
		c +=1
print("'172.16.3.1'的出现次数是{},'172.16.1.5'的出现次数是{},'17215.2.0'的出现次数是{}".format(a,b,c))
d = max(a,b,c)
if d == a:
	print('出现次数最多的是"172.16.3.1"')
elif d == b:
	print('出现次数最多的是"172.16.1.5"')
else:
	print('出现次数最多的是"172.15.2.0"')



